    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Notification $notification)
    {
        $notification->delete();

        DB::table('products')
            ->where('id', $notification->product_id)
            ->update(['rented' => 1]);

        return redirect('/notifications');
